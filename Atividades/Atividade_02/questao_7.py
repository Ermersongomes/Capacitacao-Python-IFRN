''''
7. Desenvolva um algoritmo em Python que armazene 3 notas. O programa deverá calcular e imprimir ao final a média aritmética entre elas.
'''

nota1 = float(input("Digite sua primeria nota: "))
nota2 = float(input("Digite sua segunda nota: "))
nota3 = float(input("Digite sua terceira nota: "))

media = (nota1 + nota2 + nota3) / 3

print(f'A sua média é {media:.2f}')